def node_parse(node,offset,nodeStr):
    for q in re.finditer(f"[\s]+NODE offset ([\d]+)   node x ([\d]+)  y ([\d]+)  z ([\d]+)  w ([\d]+)",nodeStr):
        offset.append(int(q.group(1)))
        x = int(q.group(2))
        if x not in node:
            node[x] = []
        factor = [int(q.group(3)),int(q.group(4)),int(q.group(5))]
        node[x].append(factor)
    return node,offset

